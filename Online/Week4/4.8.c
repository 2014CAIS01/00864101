/*
【问题描述】 要求使用数组
输入5个整数，并将它们输出。
提示：（1）定义数组；（2）使用for循环，给数组元素赋值；（3）使用for循环输出数组元素
【输入形式】从键盘输入整数
【输出形式】整数
【样例输入1】下划线表示输入
10   20   30   45   50
【样例输出1】
10   20   30   45   50    
【样例说明】 样例输出中每个整数后面有3个空格。
*/
#include <stdio.h>
int main()
{
	int i, num[5];
	for (i = 0; i < 5; i++)
		scanf("%d", &num[i]);
	for (i = 0; i < 5; i++)
		printf("%d   ", num[i]);
	return 0;
}
