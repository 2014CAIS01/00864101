/*
【问题描述】编写程序，从键盘输入两个整数，求两个整数的和、差。
【输入形式】从键盘输入两个整数，输入时两个整数之间用空格分隔。提示：先定义两个整型变量：x, y，语句为int  x,y; 然后调用输入函数scanf()，语句为：scanf("%d%d",&x,&y); 注意：语句结束时后面要加分号;
【输出形式】输出整数
【样例输入】100 30
【样例输出】130,70
【样例说明】提示：输出语句为printf("%d,%d\n",x+y,x-y);
*/
#include <stdio.h>
int main()
{
	int x, y;
	scanf("%d %d", &x, &y);
	printf("%d,%d\n", x + y, x - y);
	return 0;
}
